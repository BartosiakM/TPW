using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Etap0
{
    public class Kalkulator
    {
        public Kalkulator() { }

        public int suma(int a, int b)
        {
            return a + b;
        }

        public int roznica(int a, int b)
        {
            return a - b;
        }
    }

}
